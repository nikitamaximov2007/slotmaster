function mergeSections(original: ArticleSection[] | undefined, additions: ArticleSection[]) {
  const seen = new Set((original ?? []).map((section) => section.id));
  return [...(original ?? []), ...additions.filter((section) => !seen.has(section.id))];
}

function mergeFaq(original: FaqItem[] | undefined, additions: FaqItem[]) {
  const seen = new Set((original ?? []).map((item) => item.question));
  return [...(original ?? []), ...additions.filter((item) => !seen.has(item.question))];
}

function enrichArticle(article: Article): Article {
  const seed = articleSeeds[article.slug];
  const image = articleImageBySlug[article.slug] ?? article.image;
  if (!seed) {
    return { ...article, image, status: 'published' };
  }

  return {
    ...article,
    image,
    status: 'published',
    readingMinutes: Math.max(article.readingMinutes, seed.readingMinutes),
    body: mergeSections(article.body, buildSeoSections(article, seed)),
    faq: mergeFaq(article.faq, seed.faq),
  };
}

export const articles: Article[] = baseArticles.map(enrichArticle);

export const getArticleBySlug = (slug: string) => articles.find((a) => a.slug === slug);
export const featuredArticle = () => articles[0];
export const articleCategories = Array.from(new Set(articles.map((a) => a.category)));
export const relatedArticles = (slug: string, n = 3) => {
  const current = getArticleBySlug(slug);
  const fromList = (current?.relatedSlugs ?? [])
    .map((s) => getArticleBySlug(s))
    .filter((a): a is NonNullable<typeof a> => Boolean(a));
  if (fromList.length >= n) return fromList.slice(0, n);
  const extra = articles.filter((a) => a.slug !== slug && !fromList.includes(a)).slice(0, n - fromList.length);
  return [...fromList, ...extra];
};

